    sots: Object
})

const add_users = () => {
    router.get(route('register'));
}

const form = useForm({
    password: '',
});



const deleteUser = () => {
    const userId = usersToDelete.value;
    form.delete(route('users.destroy', { id: userId }), {
        preserveScroll: true,
        onSuccess: () => closeModal(),
        onError: () => passwordInput.value.focus(),
        onFinish: () => form.reset(),
    });
};

const closeModal = () => {
    confirmingUserDeletion.value = false;

    form.reset();
};
</script>
